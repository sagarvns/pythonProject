# Arithmetic opertions *, /,//, +, -, **, %
a = 20
b = 3
print(a * b)  # Multiply
print(a / b)  # Float division
print(a // b)  # Integer division
print(a + b)  # Add
print(a - b)  # Sub
print(a ** b)  # power
print(a % b)  # Rem
print(10 ** 0.5)  # Square Root
print(2 * (3 + 4))  # Brackets work as in maths
